//Using left shift
bool check_kth_bit(int n,int k){
    if(n&(1<<k) != 0){
        return true;
    }
    return false;
}


//Using Right shift
bool check_kth_bit(int n,int k){
    if(((n>>k)&1) != 0){
        return true;
    }
    return false;
}
