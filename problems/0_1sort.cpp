void sortOne(int ops[],int s){
    int left = 0,right = s-1;
    while(left<right){
        if(ops[left]==0){
            left++;
        }
        else if(ops[right] == 1){
            right--;
        }
        else if(ops[left] == 1 && ops[right] == 0){
            swap(ops[left],ops[right]);
            left++;
            right--;
        }
    }
}

int main(){
    int arr[8] = {0,1,1,0,0,0,1,0};

    sortOne(arr,8);
    print(arr,8);
}