class Solution {
public:
    int missingNumber(vector<int>& nums) {
        int sum = 0;
        int n = nums.size();
        for(int i = 0;i<nums.size()+1;i++){
            sum+=i;
        }
        int series_sum=0;
        for(int j  = 0;j<n;j++){
            series_sum+=nums[j];
        }
        int ans = sum - series_sum;
        return ans;
    }
};