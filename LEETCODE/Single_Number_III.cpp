class Solution {
public:
    vector<int> singleNumber(vector<int>& nums) {
        unordered_map<int,int>mp;
        vector<int>ans;
        for(auto i: nums){
            mp[i]++;
        }
        for(auto j:mp){
            if(j.second==1){
               ans.push_back(j.first);
            }
        }
        return ans;
    }
};
