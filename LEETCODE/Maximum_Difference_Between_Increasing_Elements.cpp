class Solution {
public:
    int maximumDifference(vector<int>& nums) {
        int res = nums[1]-nums[0];
        int minval = nums[0];
        for(int i = 1;i<nums.size();i++){
            res = max(res,nums[i]-minval);
            minval = min(nums[i],minval);
        }
        return res <= 0 ? -1 : res;
        
    }
};

