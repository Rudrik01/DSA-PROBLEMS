class Solution{
	public:
		vector<string> AllPossibleStrings(string s){
		    // Code here
		    vector<string> ans;
		    int n = s.length();
		    int power = pow(2,n);
		    for(int i = 1;i<power;i++){
		        string v;
		        for(int j=0;j<n;j++){
		            if(i&(1<<j)){
		                v += s[j];
		            }
		        }
		        ans.push_back(v);
		    }
		    sort(ans.begin(),ans.end());
		    return ans;
		}
};