class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        // Your code here
        int temp = m^n;
        for(int i = 0;i<31;i++){
            if((temp&(1<<i))!=0){
                return i+1;
            }
        }
        return -1;
    }
};